let currentLang = localStorage.getItem('lesoul_gh_language') || 'ko';

function setLanguage(lang) {
    currentLang = lang;
    localStorage.setItem('lesoul_gh_language', lang);
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.toggle('active', btn.dataset.lang === lang);
    });
    // 헤더의 언어 선택 드롭다운도 업데이트
    const langSelect = document.querySelector('#header-lang-select');
    if (langSelect) langSelect.value = lang;
    updateAllTranslations();
}

function t(section, key) {
    if (TRANSLATIONS[section] && TRANSLATIONS[section][key]) {
        return TRANSLATIONS[section][key][currentLang] || TRANSLATIONS[section][key]['ko'] || key;
    }
    return key;
}

function updateAllTranslations() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const [section, key] = el.dataset.i18n.split('.');
        el.textContent = t(section, key);
    });
    document.querySelectorAll('[data-i18n-section]').forEach(el => {
        const section = el.dataset.i18nSection;
        const key = el.dataset.i18nKey;
        el.textContent = t(section, key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const [section, key] = el.dataset.i18nPlaceholder.split('.');
        el.placeholder = t(section, key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        const [section, key] = el.dataset.i18nTitle.split('.');
        el.title = t(section, key);
    });
    // store name/subtitle
    if (typeof DB !== 'undefined') {
        const storeName = DB.getSetting('store_name') || 'LES SOUL';
        const storeSubtitle = DB.getSetting('store_subtitle') || {
            ko: '매장 관리', zh: '店铺管理', en: 'Store Management', ja: '店舗管理'
        };
        const nameEl = document.querySelector('.store-name');
        const subtitleEl = document.querySelector('.store-subtitle');
        if (nameEl) nameEl.textContent = storeName;
        if (subtitleEl) {
            if (typeof storeSubtitle === 'object') {
                subtitleEl.textContent = storeSubtitle[currentLang] || storeSubtitle.ko || 'Store Management';
            } else {
                subtitleEl.textContent = storeSubtitle;
            }
        }
        document.title = storeName + ' - ' + (typeof storeSubtitle === 'object' ? (storeSubtitle[currentLang] || storeSubtitle.ko) : storeSubtitle);
    }
}
